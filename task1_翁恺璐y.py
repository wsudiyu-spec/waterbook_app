def print_sorted_by_frequency():
    """按频次从大到小排序并打印所有符号"""
    print("\n=== 符号按使用频次从大到小排序 ===")
    # 核心排序逻辑：按“频次”降序排列
    # key=lambda x: x[1]['频次'] → 取字典值中的“频次”作为排序依据
    # reverse=True → 从大到小排序
    sorted_items = sorted(symbol_dict.items(), key=lambda x: x[1]['频次'], reverse=True)
    
    # 遍历排序后的结果并打印
    for idx, (num, info) in enumerate(sorted_items, 1):
        print(f"第{idx}位 | 编号{num} | 名称：{info['名称']} | 频次：{info['频次']}")
    print("===================================\n")

def main():
    print("=== 水族水书符号检索系统 ===")
    print("使用说明：")
    print("1. 输入编号(01-30)→ 查询对应符号名称和频次")
    print("2. 输入单个汉字 → 搜索包含该字的所有符号")
    print("3. 输入 sort → 按频次从大到小打印所有符号")
    print("4. 输入 q → 退出程序\n")
    
    while True:
        # 容错处理：去除首尾空格
        user_input = input("请输入检索内容（编号/关键词/sort/q退出):").strip()
        
        # 1. 安全退出：用户输入q退出程序
        if user_input.lower() == 'q':
            print("程序已退出，感谢使用！")
            break
        
        # 2. 当空输入时，显示"输入不能为空，请重新输入！"并继续循环
        if not user_input:
            print("输入不能为空，请重新输入！")
            continue
        
        # 3. 按频次排序打印
        if user_input.lower() == 'sort':
            print_sorted_by_frequency()
            continue
        
        # 4. 编号检索逻辑（字典套字典取值）
        if user_input in symbol_dict:
            info = symbol_dict[user_input]
            print(f"编号 {user_input} → 名称：{info['名称']} | 频次：{info['频次']}")
        else:
            # 5. 关键词搜索逻辑
            if len(user_input) == 1 and user_input.isalpha() and '\u4e00' <= user_input <= '\u9fff':
                matched = []
                for num, info in symbol_dict.items():
                    if user_input in info['名称']:
                        matched.append(f"编号 {num} → 名称：{info['名称']} | 频次：{info['频次']}")
                
                if matched:
                    print("搜索结果（包含该字的符号）：")
                    for item in matched:
                        print(f"  {item}")
                else:
                    print("未找到包含该字的符号！")
            else:
                print("查无此号/无效指令！")

if __name__ == "__main__":
    main()