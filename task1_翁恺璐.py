# 水书符号编号检索系统

d = {
    "01": "引贯", "02": "空蒙", "03": "大腻", "04": "尖辛", "05": "各木",
    "06": "代哇", "07": "大更", "08": "计饿", "09": "半用", "10": "代排",
    "11": "九火", "12": "休四", "13": "沙朋", "14": "八平", "15": "十平",
    "16": "杀师铜", "17": "杀伤", "18": "地转", "19": "姑秀", "20": "忌方宿",
    "21": "九星", "22": "壬辰", "23": "公溶", "24": "吉方", "25": "大旺方",
    "26": "大利方", "27": "吉立", "28": "抽条", "29": "设金堂", "30": "富癸",
}
# 循环开始
while True:
    # 1. 接收输入并去空格（必须先做！）
    user_input = input("请输入编号（输入q退出）：").strip()
    
    # 2. 优先判断退出（这行是核心，必须在编号判断前面！）
    if user_input == 'q':  # 先简化成严格匹配小写q，排除大小写干扰
        print("程序优雅退出，感谢使用！")
        break  # 终止循环，程序结束
    
    # 3. 再判断编号是否有效
    if user_input in d:
        print(f"编号 {user_input} 对应的符号：{d[user_input]}")
    else:
        print("查无此号")